import { mkdirSync, readFileSync, writeFileSync } from "node:fs";
import { dirname, resolve } from "node:path";
import { fileURLToPath } from "node:url";

const scriptDir = dirname(fileURLToPath(import.meta.url));
const projectRoot = resolve(scriptDir, "..");

const selectedMigrations = [
  "20251012122533_fdfd4eb4-8715-44a8-8fd9-501f68e62e9d.sql",
  "20251012122543_e0dca4e3-b0da-4924-90a8-2edd9cfd8102.sql",
  "20251013183426_e4fdf24a-ed9f-49e7-b238-1402f491cd24.sql",
  "20251020013814_81e82f84-0718-4d21-a0c4-af881415cbd5.sql",
  "20251020014750_ffadbceb-d154-4889-9093-bada275312af.sql",
  "20251028004505_a8f10e96-c73a-4eeb-8bca-41b0c22d4589.sql",
  "20251028230932_ca6b3ca5-092c-47b4-b6a3-6d10ac7f92c0.sql",
  "20251114200138_afca167a-07b0-4aa9-8d96-03410c092615.sql",
  "20251114201002_6433cbc7-9411-4020-8241-e09cc9cf2a79.sql",
  "20251115090000_b6a38fa1-7aa7-4eb2-9d62-empresa_relatorios.sql",
  "20260308100000_5b0f4c11-8e93-4f5f-b7f2-checklist_modelos_normalizados.sql",
  "20260308110000_a9c1b4f0-cc6f-4f4e-a750-import_checklists_from_excel.sql",
];

const migrationDir = resolve(projectRoot, "supabase", "migrations");
const outputDir = resolve(projectRoot, "supabase", "bootstrap");
const outputPath = resolve(outputDir, "new-project-bootstrap.sql");

const sections = [
  "-- Generated by scripts/build-supabase-bootstrap.mjs",
  "-- Target: blank Supabase project",
  "-- Notes:",
  "-- 1. This bootstrap intentionally skips 20251014184424_0789a8e4-b9c9-452d-984c-7bfe3ea0d1c0.sql",
  "--    because 20251028230932_ca6b3ca5-092c-47b4-b6a3-6d10ac7f92c0.sql replaces that schema.",
  "-- 2. No Supabase Edge Functions are versioned in this repository.",
  "",
  "CREATE EXTENSION IF NOT EXISTS pgcrypto;",
  "",
  "BEGIN;",
];

for (const migrationName of selectedMigrations) {
  const migrationPath = resolve(migrationDir, migrationName);
  const body = readFileSync(migrationPath, "utf8").trim();
  sections.push(`-- >>> ${migrationName}`);
  sections.push(body);
  sections.push("");
}

sections.push("COMMIT;");
sections.push("");

mkdirSync(outputDir, { recursive: true });
writeFileSync(outputPath, sections.join("\n"), "utf8");

console.log(`Bootstrap written to ${outputPath}`);
console.log(`Included migrations: ${selectedMigrations.length}`);
